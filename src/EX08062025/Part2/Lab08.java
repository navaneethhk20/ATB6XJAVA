package EX08062025.Part2;

import java.util.Scanner;

public class Lab08 {
    public static void main(String[] args) {
        //prime number
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        System.out.println("Printing prime numbers");
        for(int i =1; i<=n;i++){
            if(i%2==0){
                System.out.println();
            }else {
                System.out.println();
            }
        }
    }
}
